




// A basic variable is defined like so
// VVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVV

double x = 20.0;
// ^ The first thing is the "type", it tells the computer what kind of thing
// your variable is meant to store


double y = 20.0;
//     ^ Second thing is the name. It tells the computer how you want to refer to your variable


double z = 20.0;
//         ^ Third thing is what it should equal. This time we set it equal to a number with a decimal, because that's what doubles store.






// HOW TO USE VARIABLES IN CODE
// VVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVVV

// Whenever you could type out a value, I.E.
int w = 20;
// That number could be replaced with the name variable of the same type.
int a = w;
// The value of "a" is now 20, because that's what was stored in "w".
// When you use "=", it copies the value of the thing on the right (it doesn't just have to be a literal value) into the variable on the left
// The "value" of w right now is 20, but it could change in the future.